    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if (txtBid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID field is required");
            txtBid.requestFocus();
        } else {
            try {
                java.sql.Connection con = (java.sql.Connection) Connect.Connection();
                PreparedStatement pst = con.prepareStatement("select * from book where id=?");

                // Set the parameter value
                pst.setString(1, txtBid.getText());

                // Execute the query
                ResultSet rs = pst.executeQuery();

                if (rs.next()) {
                    txtBookName.setText(rs.getString(2));
                }

                //pst.executeUpdate();
              //  JOptionPane.showMessageDialog(this, "Record Saved Successfully");

                pst.close();
                con.close();
            } catch (SQLException ex) {
                Logger.getLogger(Connect.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnSearchActionPerformed

