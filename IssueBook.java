    private void IssueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IssueActionPerformed
        if (txtBid.getText().isEmpty() || txtIssue.getText().isEmpty() || txtStudentId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID, Issue Date, and Student ID fields are required");
        } else {
            try {
                // Establish connection
                con = Connect.Connection();
                
                // Prepare the statement
                pst = con.prepareStatement("UPDATE book SET status=?, issuedate=?, studentid=? WHERE id=?");
                
                // Set parameters
                pst.setString(1, "Issued");
                pst.setString(2, txtIssue.getText());
                pst.setString(3, txtStudentId.getText());
                pst.setString(4, txtBid.getText());
                
                // Execute the update
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Book Issued Successfully");
                
                // Clear input fields
                txtBid.setText("");
                txtBookName.setText("");
                txtStudentId.setText("");
                txtIssue.setText("");
                
                // Close resources
                pst.close();
                con.close();
            } catch (SQLException ex) {
                Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
           
        
    }//GEN-LAST:event_IssueActionPerformed

